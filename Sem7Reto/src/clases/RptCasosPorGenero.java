
package clases;

public class RptCasosPorGenero {
    private String genero;
    private int numCasosPorGenero;

    /**
     * @return the genero
     */
    public String getGenero() {
        return genero;
    }

    /**
     * @param genero the genero to set
     */
    public void setGenero(String genero) {
        this.genero = genero;
    }

    /**
     * @return the numCasosPorGenero
     */
    public int getNumCasosPorGenero() {
        return numCasosPorGenero;
    }

    /**
     * @param numCasosPorGenero the numCasosPorGenero to set
     */
    public void setNumCasosPorGenero(int numCasosPorGenero) {
        this.numCasosPorGenero = numCasosPorGenero;
    }
}
