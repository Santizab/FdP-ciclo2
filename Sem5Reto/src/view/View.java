    private void saveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUserActionPerformed
        String name = this.newName.getText();
        String lastName = this.newLastName.getText();
        try{
            editer.saveUser(null, name, lastName);
            JOptionPane.showMessageDialog(this, "Se guardo correctamente.");
            fillUserList();
            paneNewUser.setVisible(false);
        } catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage() );
        }
    }//GEN-LAST:event_saveUserActionPerformed

