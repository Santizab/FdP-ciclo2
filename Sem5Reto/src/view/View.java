    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        int user= userComboBox.getSelectedIndex();
        this.u=this.globalUserList.get(user);
        int userID=u.getId();
        boolean isCreate=true;
        final JDialog frame2 = new JDialog(this, "Formulario Historia", true);
        frame2.getContentPane().add( new Form(userID,isCreate,false,false) );
        frame2.pack();
        frame2.setVisible(true);
    }//GEN-LAST:event_createActionPerformed

