    private void fillUserList(){
        this.globalUserList = this.editer.getUserList();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        int count = 0;
        for(User u : this.globalUserList){
            model.addElement(u.getName()+ " " + u.getLastName());
            count++;
        }
        
        this.userComboBox.setModel( model );   
    }

